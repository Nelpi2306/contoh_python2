public class Nelpi {
    public static void main(String [] args){
        System.out.println("Tugas Latihan Variable Increment/Decrement");
        System.out.println();

        int a = 23;
        System.out.println("-Pre Increment-");
        System.out.println("Variable a      : "+ a);
        System.out.println("Variable a      : "+ ++a);
        System.out.println("Jadi Variable a : "+ a);

        System.out.println();

        int b = 27;
        System.out.println("-Post Increment-");
        System.out.println("Variable b      : "+ b);
        System.out.println("Variable b      : "+ b++);
        System.out.println("Jadi Variable b : "+ b);
    }
}